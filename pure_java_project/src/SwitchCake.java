public class SwitchCake {
    public static void switchCase() {
        int choice = 2;
        switch (choice) {
            case 1:
                System.out.println("Bánh ngọt");
                break;
            case 2:
                System.out.println("Bánh mặn");
                break;
            default:
                System.out.println("Không có loại bánh này.");
        }
    }
}
